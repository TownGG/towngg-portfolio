function tggAdminLang() {
  return window.tggCurrentLanguage || localStorage.getItem('towngg_language') || 'en';
}

function tggAdminApply(root = document) {
  const lang = tggAdminLang();
  if (lang === 'en') return;
  const dict = TGG_ADMIN_I18N[lang];
  if (!dict) return;

  root.querySelectorAll?.('[data-admin-i18n-html]').forEach((el) => {
    const key = el.dataset.adminI18nHtml;
    if (dict[key]) el.innerHTML = dict[key];
  });

  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent || parent.closest('script, style, input, textarea')) return NodeFilter.FILTER_REJECT;
      return dict[node.nodeValue.trim()] ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
    }
  });
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => {
    const raw = node.nodeValue.trim();
    node.nodeValue = node.nodeValue.replace(raw, dict[raw]);
  });

  root.querySelectorAll?.('input[placeholder]').forEach((input) => {
    const translated = dict[input.getAttribute('placeholder')];
    if (translated) input.setAttribute('placeholder', translated);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('h1').forEach((h1) => {
    const key = h1.innerHTML.trim();
    if (key.includes('<span>')) h1.dataset.adminI18nHtml = key;
  });
  tggAdminApply(document);
  new MutationObserver((mutations) => {
    mutations.forEach((mutation) => {
      mutation.addedNodes.forEach((node) => {
        if (node.nodeType === Node.ELEMENT_NODE) tggAdminApply(node);
      });
    });
  }).observe(document.body, { childList: true, subtree: true });
});

